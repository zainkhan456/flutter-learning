//Arithmetic operators in dart
void main() {
  int a = 10;
  int b = 3;
  print('Addition: ${a + b}');
  print('Subtraction: ${a - b}');
  print('Multiplication: ${a * b}');
  print('Division: ${a / b}');
  print('Integer Division: ${a ~/ b}');//print only integer and ignore after point numbers
  print('Modulus: ${a % b}');
}
